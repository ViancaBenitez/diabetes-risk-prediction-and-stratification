# =========================
# IMPORTS
# =========================
import streamlit as st
import joblib
import numpy as np
from datetime import date
import matplotlib.pyplot as plt
import plotly.graph_objects as go

from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer
from reportlab.lib.styles import getSampleStyleSheet

# =========================
# LOAD MODEL & SCALER
# =========================
model = joblib.load("model.pkl")
scaler = joblib.load("scaler.pkl")

# =========================
# FUNCTIONS
# =========================
def get_risk_level(prob):
    if prob < 0.3:
        return "Low Risk"
    elif prob < 0.7:
        return "Medium Risk"
    else:
        return "High Risk"

def get_recommendation(risk):
    if risk == "High Risk":
        return "Consider insulin therapy (clinical decision required)"
    elif risk == "Medium Risk":
        return "Oral medication + lifestyle modification"
    else:
        return "Lifestyle monitoring"

def get_bmi_category(bmi):
    if bmi < 18.5:
        return "Underweight"
    elif bmi < 23:
        return "Normal Weight"
    elif bmi < 25:
        return "Overweight"
    elif bmi < 30:
        return "Obese Class I"
    else:
        return "Obese Class II"

def get_glucose_category(glucose):
    if glucose < 140:
        return "Normal"
    elif glucose < 200:
        return "Prediabetes"
    else:
        return "Diabetes"

def get_dpf_category(dpf):
    if dpf < 0.3:
        return "Low Genetic Risk"
    elif dpf < 0.7:
        return "Moderate Genetic Risk"
    else:
        return "High Genetic Risk"

def generate_pdf(name, sex, date, preg, glucose, bp, skin, insulin, bmi, dpf, age, pred, prob, risk, reco):
    doc = SimpleDocTemplate("report.pdf")
    styles = getSampleStyleSheet()

    content = []
    content.append(Paragraph("Diabetes Clinical Decision Support Report", styles['Title']))
    content.append(Spacer(1, 10))

    content.append(Paragraph(f"Name: {name}", styles['Normal']))
    content.append(Paragraph(f"Sex: {sex}", styles['Normal']))
    content.append(Paragraph(f"Date: {date}", styles['Normal']))
    content.append(Spacer(1, 10))

    content.append(Paragraph("Clinical Inputs", styles['Heading2']))
    content.append(Paragraph(f"Glucose: {glucose}", styles['Normal']))
    content.append(Paragraph(f"BMI: {bmi}", styles['Normal']))
    content.append(Paragraph(f"Age: {age}", styles['Normal']))
    content.append(Spacer(1, 10))

    content.append(Paragraph("Results", styles['Heading2']))
    content.append(Paragraph(f"Prediction: {'Positive' if pred == 1 else 'Negative'}", styles['Normal']))
    content.append(Paragraph(f"Probability: {prob:.2f}", styles['Normal']))
    content.append(Paragraph(f"Risk Level: {risk}", styles['Normal']))
    content.append(Spacer(1, 10))

    content.append(Paragraph("Recommendation", styles['Heading2']))
    content.append(Paragraph(reco, styles['Normal']))

    doc.build(content)

    with open("report.pdf", "rb") as f:
        return f.read()

# =========================
# PAGE CONFIG
# =========================
st.set_page_config(page_title="Diabetes CDSS", layout="wide")

# =========================
# HEADER
# =========================
st.title("🩺 Diabetes Clinical Decision Support System")
st.markdown("### AI-Powered Risk Prediction, Stratification, and Treatment Guidance")
st.divider()

# =========================
# PATIENT DETAILS
# =========================
st.subheader("👤 Patient Details")

colA, colB, colC = st.columns(3)

with colA:
    name = st.text_input("Patient Name")

with colB:
    sex = st.selectbox("Sex", ["Female", "Male"])

with colC:
    visit_date = st.date_input("Date", value=date.today())

# =========================
# CLINICAL NOTES
# =========================
st.markdown("### 🏥 Clinical Notes")

st.markdown("""
- **BMI** uses Asia-Pacific (Filipino) classification
- **Glucose** is measured in mg/dL
- **Blood Pressure** uses *diastolic value only* (dataset limitation)
- **Insulin = 0** may indicate missing or unavailable data
- **Diabetes Pedigree Function (DPF)** – a genetic risk score based on family history
- **Sex** is not used in prediction
""")

# =========================
# INPUT SECTION
# =========================
st.subheader("🧾 Patient Information")

col1, col2, col3 = st.columns(3)

with col1:
    preg = st.number_input("Pregnancies", 0, 20)
    glucose = st.number_input("Glucose (mg/dL)", 0, 300)
    bp = st.number_input("Blood Pressure (Diastolic)", 0, 150)

with col2:
    skin = st.number_input("Skin Thickness", 0, 100)
    insulin = st.number_input("Insulin", 0, 900)
    bmi = st.number_input("BMI", 0.0, 70.0)

with col3:
    dpf = st.number_input("Diabetes Pedigree Function", 0.0, 3.0)
    age = st.number_input("Age", 1, 120)

st.divider()

# =========================
# PREDICT BUTTON
# =========================
if st.button("🔍 Analyze Patient Risk", use_container_width=True):

    input_data = np.array([[preg, glucose, bp, skin, insulin, bmi, dpf, age]])
    input_scaled = scaler.transform(input_data)

    pred = model.predict(input_scaled)[0]
    prob = model.predict_proba(input_scaled)[0][1]

    risk = get_risk_level(prob)
    reco = get_recommendation(risk)

    # =========================
    # RESULTS
    # =========================
    st.subheader(f"📊 Results for {name}" if name else "📊 Clinical Assessment Results")

    colX, colY, colZ = st.columns(3)

    with colX:
        st.markdown("#### 🧪 Diagnosis")
        if pred == 1:
            st.markdown("<div style='padding:15px;border-radius:10px;background-color:#ffcccc;color:#900;'>🔴 Positive (Diabetes Detected)</div>", unsafe_allow_html=True)
        else:
            st.markdown("<div style='padding:15px;border-radius:10px;background-color:#ccffcc;color:#060;'>🟢 Negative (No Diabetes)</div>", unsafe_allow_html=True)

    with colY:
        st.markdown("#### 📈 Probability")
        st.markdown(f"<div style='padding:15px;border-radius:10px;background-color:#e6f0ff;'><h2 style='text-align:center'>{prob:.2f}</h2></div>", unsafe_allow_html=True)

    with colZ:
        st.markdown("#### ⚠️ Risk Level")
        color = "#ccffcc" if risk == "Low Risk" else "#fff3cd" if risk == "Medium Risk" else "#ffcccc"
        icon = "🟢" if risk == "Low Risk" else "🟡" if risk == "Medium Risk" else "🔴"
        st.markdown(f"<div style='padding:15px;border-radius:10px;background-color:{color};'>{icon} <b>{risk}</b></div>", unsafe_allow_html=True)

    st.divider()

    # =========================
    # 📊 RISK CHART
    # =========================
    st.subheader("🎯 Risk Gauge")

    fig = go.Figure(go.Indicator(
        mode="gauge+number",
        value=prob * 100,
        title={'text': "Diabetes Risk (%)"},
        gauge={
            'axis': {'range': [0, 100]},
            'steps': [
                {'range': [0, 30], 'color': "green"},
                {'range': [30, 70], 'color': "yellow"},
                {'range': [70, 100], 'color': "red"},
            ],
        }
    ))

    st.plotly_chart(fig, use_container_width=True)

    st.divider()

    # =========================
    # INTERPRETATION
    # =========================
    st.subheader("📌 Clinical Interpretation")

    bmi_cat = get_bmi_category(bmi)
    glucose_cat = get_glucose_category(glucose)
    dpf_cat = get_dpf_category(dpf)

    st.write(f"**BMI Category (PH):** {bmi_cat}")
    st.write(f"**Glucose Category:** {glucose_cat}")
    st.write(f"**Genetic Risk (DPF):** {dpf_cat}")

    st.info(f"The patient is classified as **{risk}** based on model prediction. This supports (not replaces) clinical judgment.")

    st.subheader("💊 Recommended Intervention")

    if risk == "High Risk":
        st.error(reco)
    elif risk == "Medium Risk":
        st.warning(reco)
    else:
        st.success(reco)

    st.divider()

    # =========================
    # REPORT
    # =========================
    st.subheader("🧾 Patient Report")

    st.markdown(f"""
### 🧍 Patient Summary

**Name:** {name if name else 'N/A'}
**Sex:** {sex}
**Date:** {visit_date}

---

### 📊 Results

- **Prediction:** {"🔴 Positive (Diabetes)" if pred == 1 else "🟢 Negative"}
- **Probability:** {prob:.2f}
- **Risk Level:** **{risk}**

---

### 💊 Recommendation

> {reco}
""")

    pdf_data = generate_pdf(
        name, sex, visit_date,
        preg, glucose, bp, skin, insulin,
        bmi, dpf, age,
        pred, prob, risk, reco
    )

    st.download_button(
        label="⬇️ Download PDF Report",
        data=pdf_data,
        file_name=f"diabetes_report_{name if name else 'patient'}.pdf",
        mime="application/pdf"
    )

    st.divider()
    st.caption("⚠️ This system is for decision support only and does not replace clinical judgment.")