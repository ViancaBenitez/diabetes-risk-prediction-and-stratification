# =========================
# IMPORTS
# =========================
import streamlit as st
import joblib
import numpy as np
from datetime import date
import plotly.graph_objects as go
import plotly.io as pio

from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Image
from reportlab.lib.styles import getSampleStyleSheet

# =========================
# LOAD MODEL & SCALER
# =========================
model = joblib.load("model.pkl")
scaler = joblib.load("scaler.pkl")

# =========================
# CONFIG
# =========================
st.set_page_config(page_title="Diabetes CDSS", layout="wide")

# =========================
# FUNCTIONS (KEEP AS REQUESTED)
# =========================
def get_risk_level(prob):
    if prob < 0.3:
        return "Low Risk"
    elif prob < 0.7:
        return "Medium Risk"
    else:
        return "High Risk"

def get_recommendation(risk):
    if risk == "High Risk":
        return "Consider insulin therapy (clinical decision required)"
    elif risk == "Medium Risk":
        return "Oral medication + lifestyle modification"
    else:
        return "Lifestyle monitoring"

def get_bmi_category(bmi):
    if bmi < 18.5:
        return "Underweight"
    elif bmi < 23:
        return "Normal Weight"
    elif bmi < 25:
        return "Overweight"
    elif bmi < 30:
        return "Obese Class I"
    else:
        return "Obese Class II"

def get_glucose_category(glucose):
    if glucose < 140:
        return "Normal"
    elif glucose < 200:
        return "Prediabetes"
    else:
        return "Diabetes"

def get_dpf_category(dpf):
    if dpf < 0.3:
        return "Low Genetic Risk"
    elif dpf < 0.7:
        return "Moderate Genetic Risk"
    else:
        return "High Genetic Risk"

# =========================
# GAUGE
# =========================
def create_gauge(prob):
    value = prob * 100

    fig = go.Figure(go.Indicator(
        mode="gauge+number+delta",
        value=value,
        number={'suffix': "%", 'font': {'size': 50}},
        delta={'reference': 50},

        gauge={
            'axis': {'range': [0, 100]},
            'bar': {'color': "#2a5298"},
            'steps': [
                {'range': [0, 30], 'color': "#12c06a"},
                {'range': [30, 70], 'color': "#ffcc00"},
                {'range': [70, 100], 'color': "#ff4d4d"},
            ],
            'threshold': {
                'line': {'color': "black", 'width': 5},
                'value': value
            }
        }
    ))

    fig.update_layout(height=400, margin=dict(l=20, r=20, t=30, b=20))
    return fig

# =========================
# PDF FUNCTION (WITH GAUGE)
# =========================
def generate_pdf(name, sex, date, preg, glucose, bp, skin, insulin, bmi, dpf, age, pred, prob, risk, reco):

    doc = SimpleDocTemplate("report.pdf")
    styles = getSampleStyleSheet()

    bmi_cat = get_bmi_category(bmi)
    glucose_cat = get_glucose_category(glucose)
    dpf_cat = get_dpf_category(dpf)

    # Create gauge image
    fig = create_gauge(prob)
    img_path = "gauge.png"
    pio.write_image(fig, img_path, width=500, height=300)

    content = []

    content.append(Paragraph("Diabetes Clinical Decision Support Report", styles['Title']))
    content.append(Spacer(1, 15))

    content.append(Paragraph("<b>Patient Information</b>", styles['Heading2']))
    content.append(Paragraph(f"Name: {name}", styles['Normal']))
    content.append(Paragraph(f"Sex: {sex}", styles['Normal']))
    content.append(Paragraph(f"Date: {date}", styles['Normal']))
    content.append(Spacer(1, 10))

    content.append(Paragraph("<b>Risk Visualization</b>", styles['Heading2']))
    content.append(Image(img_path, width=400, height=250))
    content.append(Spacer(1, 10))

    content.append(Paragraph("<b>Clinical Inputs</b>", styles['Heading2']))
    content.append(Paragraph(f"Pregnancies: {preg}", styles['Normal']))
    content.append(Paragraph(f"Glucose: {glucose} ({glucose_cat})", styles['Normal']))
    content.append(Paragraph(f"Blood Pressure: {bp}", styles['Normal']))
    content.append(Paragraph(f"BMI: {bmi} ({bmi_cat})", styles['Normal']))
    content.append(Paragraph(f"DPF: {dpf} ({dpf_cat})", styles['Normal']))
    content.append(Paragraph(f"Age: {age}", styles['Normal']))
    content.append(Spacer(1, 10))

    content.append(Paragraph("<b>Results</b>", styles['Heading2']))
    content.append(Paragraph(f"Prediction: {'Positive (Diabetes)' if pred == 1 else 'Negative'}", styles['Normal']))
    content.append(Paragraph(f"Probability: {prob*100:.1f}%", styles['Normal']))
    content.append(Paragraph(f"<b>Risk Level: {risk}</b>", styles['Normal']))
    content.append(Spacer(1, 10))

    content.append(Paragraph("<b>Recommendation</b>", styles['Heading2']))
    content.append(Paragraph(reco, styles['Normal']))
    content.append(Spacer(1, 15))

    content.append(Paragraph("<b>Disclaimer</b>", styles['Heading2']))
    content.append(Paragraph(
        "This system is an educational clinical decision support tool. "
        "It does not replace professional medical advice.",
        styles['Normal']
    ))

    doc.build(content)

    with open("report.pdf", "rb") as f:
        return f.read()

