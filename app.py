# =========================
# LOAD MODEL & SCALER
# =========================
model = joblib.load("model.pkl")
scaler = joblib.load("scaler.pkl")

# =========================
# CONFIG
# =========================
st.set_page_config(page_title="Diabetes CDSS", layout="wide")

# =========================
# FUNCTIONS (KEEP AS REQUESTED)
# =========================
def get_risk_level(prob):
    if prob < 0.3:
        return "Low Risk"
    elif prob < 0.7:
        return "Medium Risk"
    else:
        return "High Risk"

def get_recommendation(risk):
    if risk == "High Risk":
        return "Consider insulin therapy (clinical decision required)"
    elif risk == "Medium Risk":
        return "Oral medication + lifestyle modification"
    else:
        return "Lifestyle monitoring"

def get_bmi_category(bmi):
    if bmi < 18.5:
        return "Underweight"
    elif bmi < 23:
        return "Normal Weight"
    elif bmi < 25:
        return "Overweight"
    elif bmi < 30:
        return "Obese Class I"
    else:
        return "Obese Class II"

def get_glucose_category(glucose):
    if glucose < 140:
        return "Normal"
    elif glucose < 200:
        return "Prediabetes"
    else:
        return "Diabetes"

def get_dpf_category(dpf):
    if dpf < 0.3:
        return "Low Genetic Risk"
    elif dpf < 0.7:
        return "Moderate Genetic Risk"
    else:
        return "High Genetic Risk"

# =========================
# GAUGE
# =========================
def create_gauge(prob):
    value = prob * 100

    fig = go.Figure(go.Indicator(
        mode="gauge+number",
        value=value,

        number={
            'suffix': "%",
            'font': {'size': 48, 'color': "#1e3c72"}
        },

        title={
            'text': "Diabetes Risk Level",
            'font': {'size': 20}
        },

        gauge={
            'shape': "angular",

            'axis': {
                'range': [0, 100],
                'tickvals': [0, 20, 40, 60, 80, 100],
                'tickcolor': "gray"
            },

            # invisible bar (clean look)
            'bar': {'color': "rgba(0,0,0,0)"},

            'bgcolor': "white",
            'borderwidth': 2,
            'bordercolor': "#ddd",

            'steps': [
                {'range': [0, 30], 'color': "#12c06a"},
                {'range': [30, 70], 'color': "#ffcc00"},
                {'range': [70, 100], 'color': "#ff4d4d"},
            ],

            'threshold': {
                'line': {'color': "#1e3c72", 'width': 6},
                'thickness': 0.8,
                'value': value
            }
        }
    ))

    fig.update_layout(
        height=420,
        margin=dict(l=10, r=10, t=50, b=10),
        font={'family': "Arial"}
    )

    return fig

